package com.supergenius.server.manager.third.seegle.entity;

import java.io.Serializable;

/**
 * 会议室日志
 * 
 * @author chenminchang
 * @date 2016-9-9 上午9:46:33
 */
public class ConfLogEntity implements Serializable {

	private static final long serialVersionUID = 4615742086883009275L;
	private String cid;// 会议ID
	private String cname;// 会议名称
	private String confusernum;// 用户数量
	private String usenum;// 使用总次数
	private String usetotaltime;// 使用总时长
	private String useavgtime;// 平均时长

	public String getCid() {
		return cid;
	}

	public void setCid(String cid) {
		this.cid = cid;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getConfusernum() {
		return confusernum;
	}

	public void setConfusernum(String confusernum) {
		this.confusernum = confusernum;
	}

	public String getUsenum() {
		return usenum;
	}

	public void setUsenum(String usenum) {
		this.usenum = usenum;
	}

	public String getUsetotaltime() {
		return usetotaltime;
	}

	public void setUsetotaltime(String usetotaltime) {
		this.usetotaltime = usetotaltime;
	}

	public String getUseavgtime() {
		return useavgtime;
	}

	public void setUseavgtime(String useavgtime) {
		this.useavgtime = useavgtime;
	}
}
