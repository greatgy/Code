package com.supergenius.xo.manager.entity;

import org.joda.time.DateTime;

import com.genius.model.base.entity.BaseEntity;
import com.supergenius.xo.base.enums.EUser;

/**
 * 请假记录表
 * @author XieMing
 * @date 2016-7-19 上午11:41:30
 */
public class AppLeave extends BaseEntity {

	private static final long serialVersionUID = -1178681819360479388L;
	private String useruid;//useruid
	private EUser usertype;//请假人类型（裁判/专家）
	private DateTime starttime;//请假起始时间
	private DateTime endtime;//请假结束时间
	private String desc;//请假事由说明
	private String result;//处理结果
	
	public String getUseruid() {
		return useruid;
	}
	
	public void setUseruid(String useruid) {
		this.useruid = useruid;
	}
	
	public EUser getUsertype() {
		return usertype;
	}
	
	public void setUsertype(EUser usertype) {
		this.usertype = usertype;
	}
	
	public DateTime getStarttime() {
		return starttime;
	}
	
	public void setStarttime(DateTime starttime) {
		this.starttime = starttime;
	}
	
	public DateTime getEndtime() {
		return endtime;
	}
	
	public void setEndtime(DateTime endtime) {
		this.endtime = endtime;
	}
	
	public String getDesc() {
		return desc;
	}
	
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	public String getResult() {
		return result;
	}
	
	public void setResult(String result) {
		this.result = result;
	}
	
}
