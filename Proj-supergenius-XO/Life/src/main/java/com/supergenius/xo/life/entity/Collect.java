package com.supergenius.xo.life.entity;

import com.genius.model.base.entity.BaseEntity;
import com.supergenius.xo.life.enums.ECollectType;

/**
 * 收藏实体类
 * 
 * @author YangGuang
 * @date 2018年5月9日12:35:53
 */
public class Collect extends BaseEntity {

	private static final long serialVersionUID = -3074816787819005109L;
	private String useruid; // 用户uid
	private String refuid; // 相对应文章uid
	private ECollectType type; // 类型（0文章、1话题、2视频）

	public ECollectType getType() {
		return type;
	}

	public void setType(ECollectType type) {
		this.type = type;
	}

	public String getUseruid() {
		return useruid;
	}

	public void setUseruid(String useruid) {
		this.useruid = useruid;
	}

	public String getRefuid() {
		return refuid;
	}

	public void setRefuid(String reduid) {
		this.refuid = reduid;
	}
}
