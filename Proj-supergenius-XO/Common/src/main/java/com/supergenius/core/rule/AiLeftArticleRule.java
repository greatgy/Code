package com.supergenius.core.rule;

/**
 * 天才AI左侧缓存Rule规则
 *
 * @author Yangguang
 */
public class AiLeftArticleRule extends AiArticleRlue{
	
	protected static String p = "/left";

	public AiLeftArticleRule() {
		this("", 0);
	}

	/**
	 * @param k
	 */
	public AiLeftArticleRule(String k) {
		this(k, 0);
	}

	/**
	 * @param k
	 */
	public AiLeftArticleRule(String k, int exp) {
		super(k, exp);
		super.path += p;
		refreshConfKey();
	}

}
