package com.supergenius.xo.enterpriser.entity;

import com.genius.core.base.annotation.Json;
import com.genius.model.base.entity.BaseEntity;

/** 
* 互助合作申请实体
* @author chenminchang
* @date 2016年12月5日 下午12:05:02 
*/
public class AppCooperation extends BaseEntity {

	private static final long serialVersionUID = -8244543683234585332L;
	private String participateuid; //专题讲座报名uid
	@Json(strategy = Json.webStrategy)
	private String name; //姓名
	@Json(strategy = Json.webStrategy)
	private String mobile; //手机
	@Json(strategy = Json.webStrategy)
	private String email; //邮箱
	@Json(strategy = Json.webStrategy)
	private int semester; //第几期讲座参与者
	@Json(strategy = Json.webStrategy)
	private String address; //邮寄地址
	@Json(strategy = Json.webStrategy)
	private String company; //供职单位
	@Json(strategy = Json.webStrategy)
	private String job; //职位
	private String userSn;
	
	public String getParticipateuid() {
		return participateuid;
	}
	
	public void setParticipateuid(String participateuid) {
		this.participateuid = participateuid;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getMobile() {
		return mobile;
	}
	
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
	
	public int getSemester() {
		return semester;
	}
	
	public void setSemester(int semester) {
		this.semester = semester;
	}
	
	public String getAddress() {
		return address;
	}
	
	public void setAddress(String address) {
		this.address = address;
	}
	
	public String getCompany() {
		return company;
	}
	
	public void setCompany(String company) {
		this.company = company;
	}
	
	public String getJob() {
		return job;
	}
	
	public void setJob(String job) {
		this.job = job;
	}
	
	@Json(strategy = Json.webStrategy)
	public String getUserSn() {
		return userSn;
	}
	
	public void setUserSn(String userSn) {
		this.userSn = userSn;
	}
}
