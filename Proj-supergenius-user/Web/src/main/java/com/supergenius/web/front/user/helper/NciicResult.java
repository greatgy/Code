package com.supergenius.web.front.user.helper;

public class NciicResult {
	
	/**
	 * 1:验证成功 0:验证失败 -1：系统错误
	 */
	private int code;
	
	/**
	 * 验证返回信息说明
	 */
	private String msg;
	
	/**
	 * 身份证号码
	 */
	private String idcardNum;
	
	/**
	 * 名称
	 */
	private String name;

	@SuppressWarnings("unused")
	private String xmlResult;

	public NciicResult() {
		super();

	}

	public NciicResult(int code, String msg) {
		super();
		this.code = code;
		this.msg = msg;
	}

	public NciicResult(int code, String msg, String idcardNum, String name) {
		super();
		this.code = code;
		this.msg = msg;
		this.idcardNum = idcardNum;
		this.name = name;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getIdcardNum() {
		return idcardNum;
	}

	public void setIdcardNum(String idcardNum) {
		this.idcardNum = idcardNum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
